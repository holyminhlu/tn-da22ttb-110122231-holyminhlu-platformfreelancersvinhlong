function autoTranslate(vi) {
  if (OVERRIDES[vi]) return OVERRIDES[vi];

  let en = vi;
  // Sort by length desc to replace longer phrases first
  const sorted = [...WORD_MAP].sort((a, b) => b[0].length - a[0].length);
  for (const [from, to] of sorted) {
    en = en.split(from).join(to);
  }

  // Clean up double spaces and awkward artifacts
  en = en
    .replace(/\s{2,}/g, " ")
    .replace(/\s+\./g, ".")
    .replace(/\s+,/g, ",")
    .replace(/\s+:/g, ":")
    .replace(/\(\s+/g, "(")
    .replace(/\s+\)/g, ")")
    .trim();

  // Capitalize first letter if Vietnamese was capitalized
  if (/^[A-ZÀÁẢÃẠĂẮẰẲẴẶÂẤẦẨẪẬÈÉẺẼẸÊẾỀỂỄỆÌÍỈĨỊÒÓỎÕỌÔỐỒỔỖỘƠỚỜỞỠỢÙÚỦŨỤƯỨỪỬỮỰỲÝỶỸỴĐ]/.test(vi) && en.length > 0) {
    en = en.charAt(0).toUpperCase() + en.slice(1);
  }

  return en;
}

